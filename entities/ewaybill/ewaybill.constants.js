// Build a reverse lookup: normalized state name -> code
const normalize = (s = '') =>
  s
    .toString()
    .trim()
    .toLowerCase()
    .replace(/\s+/g, ' ');

export const STATE_NAME_TO_CODE = Object.entries(STATE_CODE_MAP).reduce(
  (acc, [code, name]) => {
    acc[normalize(name)] = code;
    return acc;
  },
  {},
);

